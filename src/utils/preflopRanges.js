/**
 * 查询翻前 GTO 动作频率
 * @param {string} handName — classifyHand() 的返回值，如 "AKs"
 * @param {'sbOpen'|'bbVsOpen'|'sbVs3Bet'} scenario — 场景
 * @returns {Object} 动作频率分布
 *   sbOpen    → { open, fold }
 *   bbVsOpen  → { fold, call, threebet }
 *   sbVs3Bet  → { fold, call, fourbet }
 */
export function getPreflopFrequency(handName, scenario) {
  const { row, col } = handNameToIndex(handName)

  switch (scenario) {
    case 'sbOpen': {
      const openFreq = SB_OPEN[row][col]
      return { open: openFreq, fold: 1 - openFreq }
    }
    case 'bbVsOpen': {
      const [fold, call, threebet] = BB_VS_OPEN[row][col]
      return { fold, call, threebet }
    }
    case 'sbVs3Bet': {
      const [fold, call, fourbet] = SB_VS_3BET[row][col]
      return { fold, call, fourbet }
    }
    default:
      return { fold: 0.5, call: 0.5 }
  }
}

/**
 * 从标准化手牌名转回矩阵索引
 * @param {string} name — "AKs" / "77" / "T9o"
 * @returns {{ row: number, col: number }}
 */
const CHAR_TO_IDX = { A: 0, K: 1, Q: 2, J: 3, T: 4, '9': 5, '8': 6, '7': 7, '6': 8, '5': 9, '4': 10, '3': 11, '2': 12 }

export function handNameToIndex(name) {
  const c1 = name[0]
  const c2 = name[1]
  const idx1 = CHAR_TO_IDX[c1]
  const idx2 = CHAR_TO_IDX[c2]

  if (idx1 === undefined || idx2 === undefined) {
    return { row: 6, col: 6 } // fallback: 88
  }

  if (idx1 === idx2) {
    // 对子
    return { row: idx1, col: idx1 }
  }

  const suffix = name[2] // 's' or 'o'
  const highIdx = Math.min(idx1, idx2)
  const lowIdx = Math.max(idx1, idx2)

  // 同花 → 上三角 (row < col), 非同花 → 下三角 (row > col)
  return suffix === 's' ? { row: highIdx, col: lowIdx } : { row: lowIdx, col: highIdx }
}

/**
 * 获取 SB open 范围内所有手牌名及其频率
 * @returns {Array<{hand: string, freq: number}>}
 */
export function getSBOpenRange() {
  const RANKS = 'AKQJT98765432'
  const result = []
  for (let r = 0; r < 13; r++) {
    for (let c = 0; c < 13; c++) {
      let name
      if (r === c) name = `${RANKS[r]}${RANKS[c]}`
      else if (r < c) name = `${RANKS[r]}${RANKS[c]}s`
      else name = `${RANKS[c]}${RANKS[r]}o`
      const freq = SB_OPEN[r][c]
      if (freq > 0) result.push({ hand: name, freq })
    }
  }
  return result
}

/**
 * 根据动作频率分布做加权随机选择
 * @param {Object} distribution — { action1: prob1, action2: prob2, … }
 * @returns {string} 被选中的动作
 */
export function selectByFrequency(distribution) {
  const rand = Math.random()
  let cumulative = 0
  const entries = Object.entries(distribution)
  for (const [action, prob] of entries) {
    cumulative += prob
    if (rand < cumulative) return action
  }
  // 浮点精度兜底
  return entries[entries.length - 1][0]
}
